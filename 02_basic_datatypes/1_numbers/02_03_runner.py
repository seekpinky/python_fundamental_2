'''

If a runner runs 10 miles in 30 minutes and 30 seconds,
What is his/her average speed in kilometers per hour? (Tip: 1 mile = 1.6 km)

'''
distance_km = 10*1.6
time_hr = (30*60+30)/(60*60)
avg_speed = distance_km/time_hr

print("average speed in kilometers per hour is   " + str(avg_speed) + " kilometers per hour")